 <h3 style="text-align:left;padding:20px 0">IMPACTS </h3>
<p style="text-align:left;padding:20px 0px;">There are certain places in the world where wildlife trade is particularly threatening. These areas are called "wildlife trade hotspots". They include China's international borders, trade hubs in Eastern/Southern Africa and Southeast Asia, the eastern borders of the European Union, some markets in Mexico, parts of the Caribbean, parts of Indonesia and New Guinea, and the Solomon Islands. While these hotspots might be trouble areas at present, they also offer opportunities for great conservation success, if action and funds are well-focused. Wildlife trade alone is a major threat to some species, but its impact is frequently made worse by habitat loss and other pressures.</p>
<p style="text-align:left;padding:20px 0px;">Local wildlife is considered an important resource by many communities, often the poorest, in the developing world. Some rural households depend on wild animals for protein, trees for fuel, and both wild animals and plants for natural cures.</p>
<p style="text-align:left;padding:20px 0px;">Overexploitation of species affects the living planet in wider ways. Just as overfishing causes imbalances in the whole marine system, our complex web of life on earth depends on careful and thoughtful use of wildlife species and their habitats.</p>
<p style="text-align:left;padding:20px 0px;">Many invasive species have been purposely introduced by wildlife traders or buyers. These invasive species prey on or compete with native species and are a major threat to the balance of nature. For example, pet Burmese Python's let loose by their owners are now considered a major pest in Florida's everglades.</p>
<p style="text-align:left;padding:20px 0px;">Like marine species killed through by catch, incidental killing of animals also happens on land. For example, crude traps set for musk deer or duikers cause damage and death to a variety of animals besides those intended.</p>
