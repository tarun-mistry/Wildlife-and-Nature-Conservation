 <h3 style="text-align:left;padding:20px 0">OVERVIEW</h3>
<p style="text-align:left;padding:20px 0px;">The world is dealing with an unprecedented spike in illegal wildlife trade, threatening to overturn decades of conservation gains. Ivory estimated to weigh more than 23 metric tons - a figure that represents 2,500 elephants - was seized in the 13 largest seizures of illegal ivory in 2011. Poaching threatens the last of our wild tigers that number around 3,890.</p>
<p style="text-align:left;padding:20px 0px;">Wildlife crime is a big business. Run by dangerous international networks, wildlife and animal parts are trafficked much like illegal drugs and arms. By its very nature, it is almost impossible to obtain reliable figures for the value of illegal wildlife trade. Experts at TRAFFIC, the wildlife trade monitoring network, estimate that it runs into hundreds of millions of dollars.</p>
<p style="text-align:left;padding:20px 0px;">Some examples of illegal wildlife trade are well known, such as poaching of elephants for ivory and tigers for their skins and bones. However, countless other species are similarly overexploited, from marine turtles to timber trees. Not all wildlife trade is illegal. Wild plants and animals from tens of thousands of species are caught or harvested from the wild and then sold legitimately as food, pets, ornamental plants, leather, tourist ornaments and medicine. Wildlife trade escalates into a crisis when an increasing proportion is illegal and unsustainable - directly threatening the survival of many species in the wild.</p>
<p style="text-align:left;padding:20px 0px;">Stamping out wildlife crime is a priority for WNC because it's the largest direct threat to the future of many of the world's most threatened species. It is second only to habitat destruction in overall threats against species survival.</p>
