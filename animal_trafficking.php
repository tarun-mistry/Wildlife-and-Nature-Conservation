 <h3 style="text-align:left;padding:20px 0">CAUSES </h3>
<p style="text-align:left;padding:20px 0px;">As human populations have grown, so has the demand for wildlife. People in many countries are accustomed to a lifestyle which fuels demand for wildlife. They expect access to a variety of seafood's, leather goods, timbers, medicinal ingredients and textiles. At the other end, extreme poverty means some people see wildlife as valuable barter for trade.</p>
<p style="text-align:left;padding:20px 0px;">In 2016 alone, 1,054 rhinos were reported killed in South Africa.This figure represents a loss in rhinos of approximately 6% in South Africa, which is close to the birth rate, meaning the population remains perilously close to the tipping point.</p>
<p style="text-align:left;padding:20px 0px;">Rhino horn, elephant ivory and tiger products continue to command high prices among consumers, especially in Asia. In Vietnam, the recent myth that rhino horn can cure cancer has led to massive poaching in South Africa and pushed the price of rhino horn to rival gold.</p>
<p style="text-align:left;padding:20px 0px;">Corruption, toothless laws, weak judicial systems and light sentences allow criminal networks to keep plundering wildlife with little regard to consequences. These factors make illegal wildlife trade a low risk business with high returns. The poachers - often poor locals - are the usually the only ones caught, leaving the real masterminds and their network safe and operational with the ability to strike again.</p>
<p style="text-align:left;padding:20px 0px;">Illegal wildlife trade is driven by high profit margins and, in many cases, the high prices paid for rare species. Vulnerable wild animals are pushed further to the edge of extinction when nature can't replenish their stocks to keep up with the rate of human consumption.</p>
