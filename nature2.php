 <h3 class="title wow zoomIn" data-wow-duration="2s" data-wow-delay="0.5s" >What is <span> Air Pollution?</span></h3>
<p style="text-align:left;padding:10px 50px;">
&emsp;&emsp;&emsp;Pollution is now a common place term that our ears are attuned to. We hear about the various forms of pollution and read about it through the mass media. Air pollution is one such form that refers to the contamination of the air, irrespective of indoors or outside. A physical, biological or chemical alteration to the air in the atmosphere can be termed as pollution. It occurs when any harmful gases, dust, smoke enters into the atmosphere and makes it difficult for plants, animals and humans to survive as the air becomes dirty.
<br><br>
&emsp;&emsp;&emsp;Air pollution can further be classified into two sections- Visible air pollution and invisible air pollution. Another way of looking at Air pollution could be any substance that holds the potential to hinder the atmosphere or the well being of the living beings surviving in it. The sustainment of all things living is due to a combination of gases that collectively form the atmosphere; the imbalance caused by the increase or decrease of the percentage of these gases can be harmful for survival.
<br><br>
&emsp;&emsp;&emsp;The Ozone layer considered crucial for the existence of the ecosystems on the planet is depleting due to increased pollution. Global warming, a direct result of the increased imbalance of gases in the atmosphere has come to be known as the biggest threat and challenge that the contemporary world has to overcome in a bid for survival.
<br><br>
<b>Causes of Air pollution?</b>
<br><br>
1. Burning of Fossil Fuels: Sulfur dioxide emitted from the combustion of fossil fuels like coal, petroleum and other factory combustibles is one the major cause of air pollution. Pollution emitting from vehicles including trucks, jeeps, cars, trains, airplanes cause immense amount of pollution. We rely on them to fulfill our daily basic needs of transportation. But, there overuse is killing our environment as dangerous gases are polluting the environment. Carbon Monoxide caused by improper or incomplete combustion and generally emitted from vehicles is another major pollutant along with Nitrogen Oxides that is produced from both natural and manmade processes.
<br><br>
2. Agricultural activities: Ammonia is a very common by product from agriculture related activities and is one of the most hazardous gases in the atmosphere. Use of insecticides, pesticides and fertilizers in agricultural activities has grown quite a lot. They emit harmful chemicals into the air and can also cause water pollution.
<br><br>
3. Exhaust from factories and industries: Manufacturing industries release large amount of carbon monoxide, hydrocarbons, organic compounds, and chemicals into the air thereby depleting the quality of air. Manufacturing industries can be found at every corner of the earth and there is no area that has not been affected by it. Petroleum refineries also release hydrocarbons and various other chemicals that pollute the air and also cause land pollution.
<br><br>
4. Mining operations: Mining is a process wherein minerals below the earth are extracted using large equipments. During the process dust and chemicals are released in the air causing massive air pollution. This is one of the reasons which are responsible for the deteriorating health conditions of workers and nearby residents.
<br><br>
5. Indoor air pollution: Household cleaning products, painting supplies emit toxic chemicals in the air and cause air pollution. Have you ever noticed that once you paint walls of your house, it creates some sort of smell which makes it literally impossible for you to breathe?
<br><br>
&emsp;&emsp;&emsp;Suspended particulate matter popular by its acronym SPM, is another cause of pollution. Referring to the particles afloat in the air, SPM is usually caused by dust, combustion etc.
<br><br>
<b>Effects of Air pollution?</b>
<br><br>
1. Respiratory and heart problems: The effects of Air pollution are alarming. They are known to create several respiratory and heart conditions along with Cancer, among other threats to the body. Several millions are known to have died due to direct or indirect effects of Air pollution. Children in areas exposed to air pollutants are said to commonly suffer from pneumonia and asthma.
<br><br>
2. Global warming: Another direct effect is the immediate alterations that the world is witnessing due to Global warming. With increased temperatures worldwide, increase in sea levels and melting of ice from colder regions and icebergs, displacement and loss of habitat have already signaled an impending disaster if actions for preservation and normalization aren't undertaken soon.
<br><br>
3. Acid Rain: Harmful gases like nitrogen oxides and sulfur oxides are released into the atmosphere during the burning of fossil fuels. When it rains, the water droplets combines with these air pollutants, becomes acidic and then falls on the ground in the form of acid rain. Acid rain can cause great damage to human, animals and crops.
<br><br>
4. Eutrophication: Eutrophication is a condition where high amount of nitrogen present in some pollutants gets developed on sea's surface and turns itself into algae and and adversely affect fish, plants and animal species. The green colored alga that is present on lakes and ponds is due to presence of this chemical only.
<br><br>
5. Effect on Wildlife: Just like humans, animals also face some devastating effects of air pollution. Toxic chemicals present in the air can force wildlife species to move to new place and change their habitat. The toxic pollutants deposit over the surface of the water and can also affect sea animals.
<br><br>
6. Depletion of Ozone layer: Ozone exists in earth's stratosphere and is responsible for protecting humans from harmful ultraviolet (UV) rays. Earth's ozone layer is depleting due to the presence of chlorofluorocarbons, hydro chlorofluorocarbons in the atmosphere. As ozone layer will go thin, it will emit harmful rays back on earth and can cause skin and eye related problems. UV rays also have the capability to affect crops.
<br><br>
&emsp;&emsp;&emsp;When you try to study the sources of Air pollution, you enlist a series of activities and interactions that create these pollutants. There are two types of sources that we will take a look at: Natural sources and Man-made sources.
<br><br>
&emsp;&emsp;&emsp;Natural sources of pollution include dust carried by the wind from locations with very little or no green cover, gases released from the body processes of living beings (Carbon dioxide from humans during respiration, Methane from cattle during digestion, Oxygen from plants during Photosynthesis). Smoke from the combustion of various inflammable objects, volcanic eruptions etc along with the emission of polluted gases also make it to the list of Natural sources of Pollution.
<br><br>
&emsp;&emsp;&emsp;While looking at the man-made contributions towards air pollution, smoke again features as a prominent component. The smoke emitted from various forms of combustion like in bio mass, factories, vehicles, furnaces etc. Wastes used to create landfills generate methane that is harmful in several ways. The reactions of certain gases and chemicals also form harmful fumes that can be dangerous to the well being of living creatures.
<br><br>
<b>Solutions for Air Pollution?</b>
<br><br>
1. Use public mode of transportation: Encourage people to use more and more public modes of transportation to reduce pollution. Also, try to make use of car pooling. If you and your colleagues come from the same locality and have same timings you can explore this option to save energy and money.
<br><br>
2. Conserve energy: Switch off fans and lights when you are going out. Large amount of fossil fuels are burnt to produce electricity. You can save the environment from degradation by reducing the amount of fossil fuels to be burned.
<br><br>
3. Understand the concept of Reduce, Reuse and Recycle: Do not throw away items that are of no use to you. In-fact reuse them for some other purpose. For e.g. you can use old jars to store cereals or pulses.
<br><br>
4. Emphasis on clean energy resources: Clean energy technologies like solar, wind and geothermal are on high these days. Governments of various countries have been providing grants to consumers who are interested in installing solar panels for their home. This will go a long way to curb air pollution.
<br><br>
5. Use energy efficient devices: CFL lights consume less electricity as against their counterparts. They live longer, consume less electricity, lower electricity bills and also help you to reduce pollution by consuming less energy.
<br><br>
&emsp;&emsp;&emsp;Several attempts are being made world wide on a personal, industrial and governmental levels to curb the intensity at which Air Pollution is rising and regain a balance as far as the proportions of the foundation gases are concerned. This is a direct attempt at slacking Global warming. We are seeing a series of innovations and experiments aimed at alternate and unconventional options to reduce pollutants. Air Pollution is one of the larger mirrors of man's follies, and a challenge we need to overcome to see a tomorrow.

