 <h3 class="title wow zoomIn" data-wow-duration="2s" data-wow-delay="0.5s" >51 Wonderful Ways <span> To Conserve Water</span></h3>
<p style="text-align:left;padding:10px 50px;">

&emsp;&emsp;&emsp;Conserving water is something that we all should be doing. We take water and water supply for granted when in all actuality supply is in high demand and of limited resource very little of the Earth's natural water can actually be used for human consumption. Producing water is costly and uses those limited supplies of water available. By conserving water you can help supply more water while bringing a multitude of benefits your way.
<br><br>
&emsp;&emsp;&emsp;Take a look at 51 ways in which you can conserve water. Each of these methods of water conservation is simple and easy and certainly things that anyone can take advantage of:-<br><br>
1. Turning the water off when brushing your teeth or washing your hair can save a lot of water. In fact, as much as 160 gallons of water can be conserved each month when you turn the water off.
<br><br>
2. Purchase water-efficient products and appliances for your home. This includes dishwashers, sink systems, bathtubs and more. These products pay for themselves in no time at all.
<br><br>
3. Plant your garden in the spring and you can save since water requirement levels are less during this season.
<br><br>
4. Do not use water to defrost foods. Although many people do use this method it requires a lot of water consumption to do this.
<br><br>
5. Check for leaks. This includes the toilet and the sinks in the home. Even a small leak could cause an extensive amount of extra water usage and more money added to the water bill.
<br><br>
6. Stop using extra water when you flush by avoiding placing anything in the toilet. This includes tissue and cigarette butts. Using this method to eliminate these items will cost you an additional 5 to 6 gallons of water per flush.
<br><br>
7. Insulate your pipes. This will prevent them from freezing in the winter while also helping heat water up faster, among other benefits.
<br><br>
8. When washing clothes, make sure that you are washing full loads of laundry only. You are wasting a lot of water washing only half of a load.
<br><br>
9. Same rule applies when using the dishwasher - keep it full.
<br><br>
10. Take shorter showers. You would be surprised at that just a few minutes off your shower time could reduce the amount of water you are using greatly.
<br><br>
11. When washing your dishes (without the dishwasher) do not leave the water running to rinse.
<br><br>
12. Keep water in a jug in the fridge instead of running the tap when you want water.
<br><br>
13. Minimize the amount of time spent watering the lawn. Ensure that you are watering it only when needed.
<br><br>
14. When watering the lawn ensure that the sprinkler system is set the right way so you are not watering the gutters and watering other things that you do not want to water.
<br><br>
15. Rainwater harvesting is used by many people and it could prove to be beneficial to you, too. Consider it!
<br><br>
16. Do you know how to shut off your water system in the event of an emergency? If not it is time to learn how to do this.
<br><br>
17. Cut off the water supply to any areas in the home that you are not using.
<br><br>
18. Have regular inspections from a plumber or water specialists. He can detect any problems quickly and easily.
<br><br>
19. Teach your kids how to conserve water and help them do it. Rewards are a great incentive!
<br><br>
20. Use brooms or other tools to clean gutters instead of the water hose.
<br><br>
21. Keep in mind those hidden water sources, including broilers and hot water heat pumps.
<br><br>
22. Use a layer of mulch around your plants and they will retain moisture for a longer time period.
<br><br>
23. Consider all decorations used outside including water fountains that may consume more energy.
<br><br>
24. Adjust your water techniques with the season and save hundreds of gallons of water.
<br><br>
25. Use only one drinking glass for your water for the day.
<br><br>
26. Consider purchasing a dual-flush toilet. These technology-savvy toilets are great to promote water conservation.
<br><br>
27. Use the car wash to wash your vehicle. A lot of water is wasted doing it at home.
<br><br>
28. Match the water fill to the appropriate size of laundry that you are doing.
<br><br>
29. Using a water sensor is a smart idea.
<br><br>
30. Use rainwater to water the plants in the house.
<br><br>
31. Using the right size pans when cooking is yet another surprising way that you can save on your water consumption.
<br><br>
32. Check your hoses and pipes for leaks, cracks, and other damage.
<br><br>
33. Reuse your towels. Using them a couple of times is perfectly fine and a great way to save water.
<br><br>
34. Do not plant in areas that are hard to water.
<br><br>
35. If you have a small lawn, try to water it by hand rather than using sprinklers. This way you have more control over the amount of water.
<br><br>
36. Install water saving devices. Use low-flow shower heads on all showers and faucet aerators on all faucets to reduce your water use.
<br><br>
37. Try to use fewer plates and cups to reduce the amount of water that needs to be used.
<br><br>
38. Don't let water go down the drain while you are waiting for temperature to adjust when taking the bath.
<br><br>
39. Read your house water readings at regular interval when no water is being used and check for hidden water leaks.
<br><br>
40. Install covers on pools to avoid loss of water evaporation and check for leaks around your pumps.
<br><br>
41. Water your lawn during early parts of the day. Avoid early watering and late watering as it leads to water loss due to evaporation.
<br><br>
42. Use broom instead of hose to clean your driveways or sidewalks. This can save several gallons of water every day.
<br><br>
43. Don't use your toilet as a trash can. Every time you flush your toilet, you use up to 9-12 liters of water.
<br><br>
44. Partial loads wastes a lot of water. Don't run the washer until you have full load of laundry to wash.
<br><br>
45. Fill the sink with soapy water in the sink when washing dishes by hand. Quickly rinse under a slow-moving stream from the faucet.
<br><br>
46. Use energy efficient washing machine. It saves up to 18 gallons of water per load.
<br><br>
47. Never put the water down the drain as you may use that water for watering a plant or garden.
<br><br>
48. Teach your kids to turn the faucets off tightly after each use.
<br><br>
49. Don't over-water your lawns and don't water any faster than the soil can absorb.
<br><br>
50. Do not brush teeth in the shower. You end up wasting gallons of water as you stand in the shower for several minutes brushing your teeth. Instead, use a small cup for brushing teeth.
<br><br>
51. Provide useful suggestions to your employer and let them know of various ways that can save water.

