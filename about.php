 <h3 style="text-align:center;padding:40px 0;margin-top: 40px;">OUR VISION </h3>
<p style="text-align:left;padding:10px 50px;">1. Promote Biodiversity: 

Biodiversity is essential for a healthy and functional ecosystem. If wildlife is extracted from its natural habitat, the delicate balance of the ecosystem will be disturbed which will then lead to disastrous results.For instance, there is a wide diversity of species living in a tropical rain forest. If any species should become extinct, the food chain will be disrupted affecting all the species. For this reason, promoting biodiversity is one of the main reasons why we should protect wildlife.<br><br>
2. Beneficial For Humans:

One can learn a lot from animals which can benefit the human race. 

For instance, a lot of medicines have been derived from the chemicals produced by animals. These medicines are then used to help cure various health conditions, such as heart diseases, disorders, and other illnesses. 

In fact, based on the statistics provided by the U.S Fish and Wildlife Service, more than 25% of the medicinal prescriptions given every year contain chemicals from animals. 

For instance, there are scientists who are studying venom from the pit viper to cure the symptoms of Melanoma, and the venom from a tarantula can help fight neurological disorders.

Wildlife protection is essential because if the animal is gone, it will be impossible to study and learn from them. Unfortunately, a lot of wildlife has disappeared from earth due to human activities, such as the Bali tiger, Mexican grizzly bear, and the Japanese wolf.<br><br>

3. Conservation of Natural Habitat:

When we conserve and protect the natural habitat of wildlife species, we enrich our planet. To do so, we must keep the animals in their natural place. Conservation of natural habitats will also be beneficial for humans since it helps keep the essential watersheds intact and ensuring clean, fresh water.

Today, there are now wildlife preservation programs wherein they allow the animals to roam freely in their natural habitat. Some of these programs also allow the animals to interact with humans. This is beneficial since it educates the people and raises awareness regarding the importance of protecting these wildlife species.

These are just some of the reasons why everyone should work together to protect the different species of wildlife. Thankfully, there are now national parks that provide the best natural habitats for various species.</p>

